export interface IContext {
  baseImageUrl: string;
}

export const MediaContext = createContext<IContext | null>(null);
export function MediaContextProvider({ children }: { children: React.ReactNode | React.ReactNode[] }) {
  const baseImageUrl = "https://image.tmdb.org/t/p/original/";

  return (
    <MediaContext.Provider
      value={{
        baseImageUrl,
      }}
    >
      {children}
    </MediaContext.Provider>
  );
}
